#include "libft.h"

int		ft_putstr_int(char *s, int fd)
{
	int count;

	count = 0;
	if (!s)
		return (count);
	while (*s)
	{
		ft_putchar_fd(*s, fd);
		s++;
		count++;
	}
	return (count);
}